public class Stammdaten {

    private String tabName;
    private String Kreditnehmer;
    private String Postleitzahl;
    private String Sitz;
    private String Staat;
    private String ISO;
    private String KreditgeberUE;
    private String KreditgeberUG;
    private String Geburtsdatum;
    private String Beruf;

    public String getTabName() {
        return tabName;
    }

    public void setTabName(String tabName) {
        this.tabName = tabName;
    }

    public String getKreditnehmer() {
        return Kreditnehmer;
    }

    public void setKreditnehmer(String Kreditnehmer) {
        this.Kreditnehmer = Kreditnehmer;
    }

    public String getPostleitzahl() {
        return Postleitzahl;
    }

    public void setPostleitzahl(String Postleitzahl) {
        this.Postleitzahl = Postleitzahl;
    }

    public String getSitz() {
        return Sitz;
    }

    public void setSitz(String Sitz) {
        this.Sitz = Sitz;
    }

    public String getStaat() {
        return Staat;
    }

    public void setStaat(String Staat) {
        this.Staat = Staat;
    }

    public String getISO() {
        return ISO;
    }

    public void setISO(String ISO) {
        this.ISO = ISO;
    }

    public String getKreditgeberUE() {
        return KreditgeberUE;
    }

    public void setKreditgeberUE(String KreditgeberUE) {
        this.KreditgeberUE = KreditgeberUE;
    }

    public String getKreditgeberUG() {
        return KreditgeberUG;
    }

    public void setKreditgeberUG(String KreditgeberUG) {
        this.KreditgeberUG = KreditgeberUG;
    }

    public String getGeburtsdatum() {
        return Geburtsdatum;
    }

    public void setGeburtsdatum(String Geburtsdatum) {
        this.Geburtsdatum = Geburtsdatum;
    }

    public String getBeruf() {
        return Beruf;
    }

    public void setBeruf(String Beruf) {
        this.Beruf = Beruf;
    }
}
